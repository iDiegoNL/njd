    <h4>1) Duik alleen waar toezicht is</h4>
    <p>Wil je op 1 januari het koude water trotseren voor een Nieuwjaarsduik, doe dit dan bij een georganiseerde duik waar ook gekwalificeerd toezicht van de Reddingsbrigade aanwezig is.
        Ga niet ‘wildduiken’ op afgelegen locaties waar geen toezicht of hulp in de buurt is. Voor IJburg is BootBurg Outdoor Workouts de organiserende partij. </p>
    <h4>2) Kom op tijd en wees goed voorbereid</h4>
    <p> Nieuwjaarsduiken worden vaak goed bezocht door zowel deelnemers als toeschouwers. Hierdoor kan de lokale infrastructuur overbelast raken. Zorg dus dat je op tijd bent en bekijk de duiklocatie vooraf.
        Dan kan je goed voorbereid het water in en er ook zo snel mogelijk weer uit. Doe een warming up, hou je aan de aanwijzingen van de organisatie en kijk waar je zo nodig om hulp kunt vragen.
        BootBurg Outdoor Workouts verzorgd voor elk startveld een korte warming-up zodat jij goed opgewarmd het water in kan. </p>
    <h4>3) Doe alleen mee aan de Nieuwjaarsduik als je je fit voelt </h4>
    <p>Indien je je niet fit voelt of het medisch niet verantwoord is om te duiken, duik dan niet. Het is niet alleen een risico voor jezelf, maar ook voor andere duikers en de hulpverleners.
        Indien je tijdens of na de duik onwel wordt, meld je dan meteen bij de EHBO of een huisartsenpost. Je kan je beter op tijd even laten nakijken dan (te) laat behandeld worden.
        Op IJburg is gezorgd voor een professioneel team van EHBOers en de Amsterdamse Reddingsbrigade zal op het strand en in het water zichtbaar aanwezig zijn. </p>
